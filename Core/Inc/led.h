typedef struct		//	structure for color value (defined over 24 bits, 8 bits per color)
{
	uint8_t r;
	uint8_t g;
	uint8_t b;
}color;

typedef struct		//	structure for one LED
{
	uint8_t g[8];
	uint8_t r[8];
	uint8_t b[8];
}led_strip;

typedef struct
{
	uint8_t first_loop;
	uint16_t led_counter;
	uint8_t led_direction;
}S_EFFECTS;

void Init_var();
