void chenillard(color bgrd_color, color moving_color, uint8_t speed);
void Pulse(color pulse_color, uint8_t speed);
void Brighten(color brighten_color, uint8_t speed);
void Dim(color dim_color, uint8_t speed);
void led_effect();
void led_init();
void led_send();
void led_set_test();
